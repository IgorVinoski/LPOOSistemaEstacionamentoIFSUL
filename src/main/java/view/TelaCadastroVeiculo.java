private void preencherCampos(Veiculo veiculo) {
    if (veiculo != null) {
        // Preenche os campos com os dados do veículo
        txfPlaca.setText(veiculo.getPlaca());
        
        // Preenche o campo Modelo
        cmbModelo.setSelectedItem(veiculo.getModelo());
        
        // Preenche o campo Proprietário
        cmbPessoasCadastradas.setSelectedItem(veiculo.getProprietario());
        
        // Se for um veículo oficial, habilita os campos de Chassi e Renavam
        if (veiculo instanceof VeiOficial) {
            cbxVeiculoOficial.setSelected(true);
            txfChassi.setText(((VeiOficial) veiculo).getChassi());
            txfRenavam.setText(((VeiOficial) veiculo).getRenavan());
            txfChassi.setEnabled(true);
            txfRenavam.setEnabled(true);
        } else {
            cbxVeiculoOficial.setSelected(false);
            txfChassi.setText("");
            txfRenavam.setText("");
            txfChassi.setEnabled(false);
            txfRenavam.setEnabled(false);
        }
    }
}

