private void carregarModelos() {
    List<Modelo> modelos = persistenciaJPA.getEntityManager().createQuery("SELECT m FROM Modelo m", Modelo.class).getResultList();
    cmbModelo.removeAllItems();
    for (Modelo modelo : modelos) {
        cmbModelo.addItem(modelo);
    }
}


    private void carregarPessoas() {
        List<Pessoa> pessoas = persistenciaJPA.getEntityManager().createQuery("SELECT p FROM Pessoa p", Pessoa.class).getResultList();
        cmbPessoasCadastradas.removeAllItems();
        for (Pessoa pessoa : pessoas) {
            cmbPessoasCadastradas.addItem(pessoa);
        }
    }

    private void configurarCamposOficiais() {
        cbxVeiculoOficial.addActionListener(evt -> {
            boolean isOficial = cbxVeiculoOficial.isSelected();
            txfChassi.setEnabled(isOficial);
            txfRenavam.setEnabled(isOficial);

            if (!isOficial) {
                txfChassi.setText("");
                txfRenavam.setText("");
            }
        });

        // Inicialmente desativa os campos RENAVAM e CHASSI
        txfChassi.setEnabled(false);
        txfRenavam.setEnabled(false);
    }

