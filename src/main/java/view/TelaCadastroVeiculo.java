private Veiculo buscarVeiculoPorPlaca(String placa) {
    try {
        List<Veiculo> veiculos = persistenciaJPA.getEntityManager()
                .createQuery("SELECT v FROM Veiculo v WHERE v.placa = :placa", Veiculo.class)
                .setParameter("placa", placa)
                .getResultList();
        return veiculos.isEmpty() ? null : veiculos.get(0);
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao buscar veículo: " + e.getMessage());
        return null;
    }
}
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
  try {
        // Verifica se a placa foi preenchida
        String placa = txfPlaca.getText().trim();
        if (placa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "A placa do veículo é obrigatória!");
            return; // Impede o cadastro se a placa não for preenchida
        }

        // Verifica se a placa já está cadastrada
            Veiculo veiculoExistente = buscarVeiculoPorPlaca(placa);
        if(veiculoExistente != null && (veiculo == null || veiculoExistente.getId() != veiculo.getId())) {
         JOptionPane.showMessageDialog(this, "Já existe um veículo cadastrado com essa placa!");
         return;
     }

      

        // Verifica se é veículo oficial
        boolean isOficial = cbxVeiculoOficial.isSelected();

        // Se o veículo não existe, cria um novo
        if (veiculo == null) {
            if (isOficial) {
                veiculo = new VeiOficial();
                ((VeiOficial) veiculo).setRenavan(txfRenavam.getText());
                ((VeiOficial) veiculo).setChassi(txfChassi.getText());
            } else {
                veiculo = new Veiculo();
            }
        } else {
            // Se o veículo já existe, apenas atualiza
            if (isOficial) {
                ((VeiOficial) veiculo).setRenavan(txfRenavam.getText());
                ((VeiOficial) veiculo).setChassi(txfChassi.getText());
            }
        }

        // Configurações comuns para ambos os tipos
        veiculo.setPlaca(placa);
        veiculo.setCor("Cor"); // Exemplo
        veiculo.setModelo((Modelo) cmbModelo.getSelectedItem());
        veiculo.setProprietario((Pessoa) cmbPessoasCadastradas.getSelectedItem());

        // Persistência no banco de dados
        persistenciaJPA.getEntityManager().getTransaction().begin();
      if (veiculo.getId() == 0) {
            persistenciaJPA.getEntityManager().persist(veiculo); // Cadastro novo
        } else {
            veiculo = persistenciaJPA.getEntityManager().merge(veiculo); // Atualiza o veículo
        }


        persistenciaJPA.getEntityManager().getTransaction().commit();

        JOptionPane.showMessageDialog(this, "Veículo salvo com sucesso!");
        limparCampos();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao salvar o veículo: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSalvarActionPerformed
